public class Person {
    // declare all attributes

    String fName;
    String lName;
    int age;
    String house;

    // method constructor
    public Person(String fName, String lName, int age, String house) {
        this.fName = fName;
        this.lName = lName;
        this.age = age;
        this.house = house;
    }
}

